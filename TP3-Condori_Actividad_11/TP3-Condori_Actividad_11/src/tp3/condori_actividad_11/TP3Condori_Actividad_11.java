package tp3.condori_actividad_11;
public class TP3Condori_Actividad_11 {
    public static void main(String[] args) {
        System.out.println("Todos los numeros de l a 100 al 0 con decrementos de 23");
        for (int i = 100; i >= 0; i -= 23){
            System.out.println(i);
        }
    }
    
}
